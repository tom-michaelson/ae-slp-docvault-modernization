async def setup_csharp_publishing(container: dagger.Container) -> dagger.Container:
    """Set up .NET/C# specific publishing tools."""
    # Install CodeArtifact credential provider
    container = (
        container.with_exec(["dotnet", "tool", "install", "-g", "AWS.CodeArtifact.NuGet.CredentialProvider"])
        .with_env_variable("PATH", "$PATH:/root/.dotnet/tools", expand=True)
        .with_exec(["dotnet", "codeartifact-creds", "install"])
    )

    return container


async def package_csharp_sdk(container: dagger.Container, version: str) -> dagger.Container:
    """Package the C# SDK."""
    logger.info(f"Packaging C# SDK version {version}...")

    # Only pass version, rely on script defaults for everything else
    return container.with_exec(
        [
            "uv",
            "run",
            "python",
            "scripts/sdk/package_sdk_csharp.py",
            "--version",
            version,
        ],
    )


async def publish_csharp_sdk(container: dagger.Container, version: str) -> dagger.Container:
    """Publish the C# SDK to AWS CodeArtifact."""
    logger.info(f"Publishing C# SDK version {version} to CodeArtifact...")

    # Only pass version, rely on script defaults for everything else
    cmd = [
        "uv",
        "run",
        "python",
        "scripts/sdk/publish_sdk_csharp.py",
        "--version",
        version,
        "--skip-duplicate-check",  # We've already checked in the pipeline
    ]

    # Add AWS profile if set
    aws_profile = os.getenv("AWS_PROFILE")
    if aws_profile:
        cmd.extend(["--aws-profile", aws_profile])

    return container.with_exec(cmd)


async def package_python_sdk(container: dagger.Container, version: str) -> dagger.Container:
    """Package the Python SDK."""
    logger.info(f"Packaging Python SDK version {version}...")

