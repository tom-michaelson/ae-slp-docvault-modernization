#!/usr/bin/env python3
"""Dagger pipeline for publishing SDK packages to AWS CodeArtifact.

This pipeline assumes the SDK has already been generated by the Temporal workflow.
It reads the SDK configuration, packages the enabled SDKs, and publishes them to CodeArtifact.
"""

import asyncio
import logging
import os
import sys
from pathlib import Path
from typing import Any

import dagger
import yaml

from pipelines.common import setup_base_dependencies
from scripts.sdk.aws_auth_helper import configure_aws_credentials_for_dagger

# Set up logging
logger = logging.getLogger(__name__)
logging.basicConfig(level=logging.INFO)


def configure_aws_credentials(container: dagger.Container, client: dagger.Client) -> dagger.Container:
    """Configure AWS credentials for a container using available authentication methods.

    This is a wrapper around the shared helper for backward compatibility.

    Args:
        container: The dagger container to configure
        client: The dagger client for accessing host resources

    Returns:
        The container with AWS credentials configured

    """
    return configure_aws_credentials_for_dagger(container, client)


async def read_sdk_config(src_dir: dagger.Directory, client: dagger.Client) -> dict[str, Any]:  # noqa: ARG001
    """Read and parse the SDK configuration file."""
    config_content = await src_dir.file("awa/sdk/sdk.config.yaml").contents()
    return yaml.safe_load(config_content)


async def get_sdk_version(src_dir: dagger.Directory, client: dagger.Client) -> str:  # noqa: ARG001
    """Get the SDK version from the version files generated by the Temporal workflow."""
    version_files = await src_dir.directory("sdk_dist/.hash").entries()

    # Filter for .version files
    version_files = [f for f in version_files if f.endswith(".version")]

    if not version_files:
        logger.error("No version files found. The GenerateSdkWorkflow must be run first.")
        sys.exit(1)

    # Sort to get the latest version
    version_files.sort()
    latest_version = version_files[-1].replace(".version", "")

    logger.info(f"Using SDK version: {latest_version}")
    return latest_version


async def setup_base_container(client: dagger.Client, src: dagger.Directory) -> dagger.Container:
    """Set up the base container with all required tools and dependencies."""
    # Get AWS region configuration from environment
    aws_region = os.getenv("AWS_REGION", os.getenv("AWS_DEFAULT_REGION", "us-east-1"))

    # Use the shared function to set up base dependencies
    container = setup_base_dependencies(
        client=client,
        src=src,
        python_version="3.12",
    )

    # Add AWS-specific environment variables
    container = container.with_env_variable("PYTHONUNBUFFERED", "1")
    container = container.with_env_variable("AWS_DEFAULT_REGION", aws_region)
    container = container.with_env_variable("AWS_REGION", aws_region)

    # Install pyyaml needed for SDK scripts
    container = container.with_exec(["pip", "install", "pyyaml"])

    # Install AWS CLI (architecture-aware) - needed for CodeArtifact
    container = (
        container.with_exec(
            [
                "sh",
                "-c",
                "arch=$(uname -m) && "
                'if [ "$arch" = "aarch64" ] || [ "$arch" = "arm64" ]; then '
                "  curl -sL https://awscli.amazonaws.com/awscli-exe-linux-aarch64.zip -o awscliv2.zip; "
                "else "
                "  curl -sL https://awscli.amazonaws.com/awscli-exe-linux-x86_64.zip -o awscliv2.zip; "
                "fi",
            ],
        )
        .with_exec(["unzip", "-q", "awscliv2.zip"])
        .with_exec(["./aws/install"])
        .with_exec(["rm", "-rf", "awscliv2.zip", "aws"])
    )

    # Install .NET SDK for C# publishing
    container = (
        container.with_exec(["curl", "-fsSL", "https://dot.net/v1/dotnet-install.sh", "-o", "dotnet-install.sh"])
        .with_exec(["chmod", "+x", "dotnet-install.sh"])
        .with_exec(["./dotnet-install.sh", "--channel", "8.0", "--install-dir", "/usr/share/dotnet"])
        .with_exec(["ln", "-s", "/usr/share/dotnet/dotnet", "/usr/local/bin/dotnet"])
        .with_env_variable("DOTNET_ROOT", "/usr/share/dotnet")
        .with_env_variable("DOTNET_CLI_TELEMETRY_OPTOUT", "1")
    )

    # Mount source code
    container = container.with_mounted_directory("/app", src).with_workdir("/app")

    # Install Python dependencies with uv
    container = container.with_exec(["uv", "sync"])

    # Configure AWS credentials
    container = configure_aws_credentials(container, client)

    return container


async def check_python_version_exists(
    client: dagger.Client,
    version: str,
    package_name: str = "awa-client",
    domain: str = "slalom-all",
    domain_owner: str = "825505919920",
    repository: str = "slalom-pypi",
) -> bool:
    """Check if a Python SDK version exists in CodeArtifact."""
    logger.info(f"Checking if Python SDK {package_name} v{version} exists in CodeArtifact...")

    # Build AWS CLI command
    cmd = [
        "aws",
        "codeartifact",
        "describe-package-version",
        "--domain",
        domain,
        "--domain-owner",
        domain_owner,
        "--repository",
        repository,
        "--package",
        package_name,
        "--format",
        "pypi",
        "--package-version",
        version,
    ]

    # Set up AWS credentials
    container = client.container().from_("amazon/aws-cli:latest")
    container = configure_aws_credentials(container, client)

    # Set AWS region
    aws_region = os.getenv("AWS_REGION", os.getenv("AWS_DEFAULT_REGION", "us-east-1"))
    container = container.with_env_variable("AWS_DEFAULT_REGION", aws_region)

    # Execute command and check exit code
    try:
        exec_container = container.with_exec(cmd)
        exit_code = await exec_container.exit_code()

        if exit_code == 0:
            logger.info(f"✓ Python SDK v{version} already exists in CodeArtifact")
            return True

        # Non-zero exit code typically means package doesn't exist
        logger.info(f"✓ Python SDK v{version} does not exist (ready to publish)")
        return False
    except dagger.ExecError:
        # Command failed, likely because package doesn't exist
        logger.info(f"✓ Python SDK v{version} does not exist (ready to publish)")
        return False


